import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';

const API_ROOT = process.env.SRD_API_ROOT ?? 'https://www.dnd5eapi.co/api/2014';
const API_ORIGIN = new URL(API_ROOT).origin;
const OUTPUT_PATH = path.resolve('src/data/srd/srd-dataset.json');

function toApiUrl(value) {
  if (value.startsWith('http')) return value;
  if (value.startsWith('/api')) return `${API_ORIGIN}${value}`;
  return `${API_ROOT}/${value.replace(/^\//, '')}`;
}

async function fetchJson(value) {
  const url = toApiUrl(value);
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed ${response.status} ${response.statusText}: ${url}`);
  }
  return response.json();
}

async function fetchCollection(pathname) {
  const list = await fetchJson(pathname);
  const results = Array.isArray(list.results) ? list.results : [];
  const full = [];

  for (const entry of results) {
    full.push(await fetchJson(entry.url));
  }

  return full;
}

function text(value) {
  if (Array.isArray(value)) return value.filter(Boolean).join('\n');
  if (typeof value === 'string') return value;
  return '';
}

function names(values) {
  if (!Array.isArray(values)) return [];
  return values.map((value) => value?.name).filter(Boolean);
}

function compactObject(object) {
  return Object.fromEntries(Object.entries(object).filter(([, value]) => {
    if (value === undefined || value === null || value === '') return false;
    if (Array.isArray(value) && value.length === 0) return false;
    return true;
  }));
}

function normalizeSpell(spell) {
  return compactObject({
    index: spell.index,
    name: spell.name,
    level: spell.level,
    school: spell.school?.name,
    classes: names(spell.classes),
    subclasses: names(spell.subclasses),
    range: spell.range,
    duration: spell.duration,
    concentration: spell.concentration,
    ritual: spell.ritual,
    attackType: spell.attack_type,
    saveType: spell.dc?.dc_type?.name,
    damageType: spell.damage?.damage_type?.name,
    areaType: spell.area_of_effect?.type,
    areaSize: spell.area_of_effect?.size,
    desc: text(spell.desc),
    higherLevel: text(spell.higher_level),
    sourceUrl: spell.url,
  });
}

function normalizeMagicItem(item) {
  return compactObject({
    index: item.index,
    name: item.name,
    category: item.equipment_category?.name,
    rarity: item.rarity?.name,
    variants: names(item.variants),
    variant: item.variant,
    desc: text(item.desc),
    sourceUrl: item.url,
  });
}

function normalizeEquipment(item) {
  return compactObject({
    index: item.index,
    name: item.name,
    category: item.equipment_category?.name,
    gearCategory: item.gear_category?.name,
    toolCategory: item.tool_category,
    weaponCategory: item.weapon_category,
    weaponRange: item.weapon_range,
    categoryRange: item.category_range,
    damageDice: item.damage?.damage_dice,
    damageType: item.damage?.damage_type?.name,
    twoHandedDamageDice: item.two_handed_damage?.damage_dice,
    twoHandedDamageType: item.two_handed_damage?.damage_type?.name,
    properties: names(item.properties),
    armorCategory: item.armor_category,
    armorClassBase: item.armor_class?.base,
    dexBonus: item.armor_class?.dex_bonus,
    maxBonus: item.armor_class?.max_bonus,
    strMinimum: item.str_minimum,
    stealthDisadvantage: item.stealth_disadvantage,
    cost: item.cost ? `${item.cost.quantity} ${item.cost.unit}` : undefined,
    weight: item.weight,
    desc: text(item.desc),
    sourceUrl: item.url,
  });
}

console.log('Fetching SRD spells, magic items, and equipment from dnd5eapi.co...');
const [spells, magicItems, equipment] = await Promise.all([
  fetchCollection('spells'),
  fetchCollection('magic-items'),
  fetchCollection('equipment'),
]);

const dataset = {
  metadata: {
    generatedAt: new Date().toISOString(),
    apiRoot: API_ROOT,
    source: 'D&D 5e SRD API by 5e-bits / dnd5eapi.co',
    note: 'Generated by npm run import:srd. This file is a local SRD data snapshot for Vex item generation tables.',
  },
  spells: spells.map(normalizeSpell).sort((a, b) => a.name.localeCompare(b.name)),
  magicItems: magicItems.map(normalizeMagicItem).sort((a, b) => a.name.localeCompare(b.name)),
  equipment: equipment.map(normalizeEquipment).sort((a, b) => a.name.localeCompare(b.name)),
};

await mkdir(path.dirname(OUTPUT_PATH), { recursive: true });
await writeFile(OUTPUT_PATH, `${JSON.stringify(dataset, null, 2)}\n`, 'utf8');

console.log(`Wrote ${OUTPUT_PATH}`);
console.log(`Spells: ${dataset.spells.length}`);
console.log(`Magic items: ${dataset.magicItems.length}`);
console.log(`Equipment: ${dataset.equipment.length}`);
